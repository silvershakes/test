package control_statement;

public class Switch1 {

	public static void main(String[] args) {
		//year of studying
		
		int year = 2;
		switch (year)
		{
		case 1 : System.out.println("you are in 1st year");
			
		break;
		
		case 2: System.out.println("you are in 2nd year");
			
		break;
			
		case 3: System.out.println("you are in 3rd year");

		break;
		
		case 4: System.out.println("you are in 4th year");
		
		break;
		
		default:System.out.println("please enter values betwwen 1 to 4");
			
		break;
		}

	}

}
